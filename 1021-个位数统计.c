#include<stdio.h>
#include<string.h>

int main()
{
    int digit[10]={0};
    int i,n;
    char str[1001];
    scanf("%s",str);
    
    n=strlen(str);
    for(i=0;i<n;i++)
        digit[str[i]-'0']++;

    for(i=0;i<10;i++)
    {
        if(digit[i]!=0)
            printf("%d:%d\n",i,digit[i]);
    }

    return 0;
}
    

