/* 过3位数的正整数。例如234应该被输出为BBSSS1234,因为它有2个“百”、3个“十”、以及个位的4.          */
/* 输入格式：每个测试输入包含1个测试用例，给出正整数(<1000)。                                    */
/* 输出格式：每个测试用例的输出占一行，用规定的格式输出n。                                       */
/* 输入样例1：                                                                                   */
/* 234                                                                                           */
/* 输出样例1：                                                                                   */
/* BBSSS1234                                                                                     */
/* 输入样例2：                                                                                   */
/* 23                                                                                            */
/* 输出样例2：                                                                                   */
/* SS123                                                                                         */
