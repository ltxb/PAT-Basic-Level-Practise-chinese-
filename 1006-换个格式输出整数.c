#include<stdio.h>

void FormatOutput(int);

int main()
{
    int n;
    scanf("%d",&n);
    FormatOutput(n);
    return 0;
}

void FormatOutput(int n)
{
    int a[3]={0,0,0};
    int i=2,j=1;
    if(n<=0||n>=1000)
        printf("输入数字不符合要求，应是小于1000的正整数!\n");
    else
    {
        while(n!=0)
        {
           a[i]=n%10;
           n=n/10;
           i--;
        }

        for(i=0;i<3;i++)
        {
            while(a[i]>0)
	    {
	       if(i==0)
                   printf("B");
               else if(i==1)
                   printf("S");
               else
               {
                   printf("%d",j);
                   j++;
               }
               a[i]--;
            }
         }
      }
}
